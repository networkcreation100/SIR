import { chromium } from 'playwright';
const url = 'https://ask-screenshots-personally-mods.trycloudflare.com';
const browser = await chromium.launch({ headless: true });
const page = await browser.newPage({ viewport: { width: 1440, height: 1100 }, acceptDownloads: true });
const consoleErrors = [];
const pageErrors = [];
page.on('console', msg => { if (msg.type() === 'error') consoleErrors.push(`${msg.type()}: ${msg.text()}`); });
page.on('pageerror', err => pageErrors.push(err.message));
await page.goto(url, { waitUntil: 'networkidle', timeout: 60000 });
await page.getByText('Sender composer').waitFor();
await page.locator('form.composer').getByRole('button', { name: 'Send reminder' }).click();
await page.getByLabel('Send Reminder panel').waitFor();
await page.getByText('Delivery panel').waitFor();
const bodyBefore = await page.locator('body').innerText();
for (const forbidden of ['Alex Rivera', 'Morgan Chen', 'Taylor Brooks', 'alex.rivera@example.com', 'morgan.chen@example.com', 'taylor.brooks@example.com', '+1 555 010 1200', '+1 555 010 3488', '+1 555 010 8842', 'Suggested recipients', 'Select contacts below']) {
  if (bodyBefore.includes(forbidden)) throw new Error(`Removed sample contact still visible: ${forbidden}`);
}
const box = page.getByRole('textbox', { name: 'Recipients' });
if ((await box.inputValue()) !== '') throw new Error('Recipient box should start empty');
if ((await box.getAttribute('placeholder')) !== 'Enter email addresses separated by commas') throw new Error('Email placeholder should be generic');
await page.getByText('Enter recipients above; they will appear here automatically.').waitFor();
await box.fill('first@example.com, second@example.com');
await page.getByText('2 recipients ready.').waitFor();
await page.getByText('first@example.com').waitFor();
await page.getByText('second@example.com').waitFor();
if (await page.getByRole('button', { name: /Package \+ send/i }).isDisabled()) throw new Error('Package + send should enable for valid typed emails');
await page.getByRole('button', { name: /^Text/i }).click();
if ((await box.inputValue()) !== '') throw new Error('Phone box should start empty and not inherit email recipients');
if ((await box.getAttribute('placeholder')) !== 'Enter phone numbers separated by commas') throw new Error('Phone placeholder should be generic');
await box.fill('+1 808 555 1212, +1 808 555 3434');
await page.getByText('2 recipients ready.').waitFor();
await page.getByText('+1 808 555 1212').waitFor();
await page.getByText('+1 808 555 3434').waitFor();
if (await page.getByRole('button', { name: /Package \+ send/i }).isDisabled()) throw new Error('Package + send should enable for valid typed phones');
await box.fill('+1 808');
await page.getByText('Use valid phone numbers separated by commas.').waitFor();
if (!(await page.getByRole('button', { name: /Package \+ send/i }).isDisabled())) throw new Error('Package + send should disable for invalid phone');
await page.screenshot({ path: 'scripts/delivery-panel-sync-audit.png', fullPage: true });
console.log(JSON.stringify({ ok: true, url, covered: ['removed sample names/emails/phones','generic placeholders','recipient display syncs from box','email recipients validate from typed box','text recipients validate from typed box','invalid typed phone disables send'], consoleErrors, pageErrors, screenshot: 'scripts/delivery-panel-sync-audit.png' }, null, 2));
if (consoleErrors.length || pageErrors.length) process.exitCode = 1;
await browser.close();
