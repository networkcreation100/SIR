import { chromium } from 'playwright';
const url = 'https://treasures-helps-avon-mpegs.trycloudflare.com';
const browser = await chromium.launch({ headless: true });
const page = await browser.newPage({ viewport: { width: 1366, height: 900 } });
const consoleErrors = [];
const pageErrors = [];
page.on('console', msg => { if (msg.type() === 'error') consoleErrors.push(`${msg.type()}: ${msg.text()}`); });
page.on('pageerror', err => pageErrors.push(err.message));
await page.goto(url, { waitUntil: 'networkidle', timeout: 60000 });
await page.getByText('Sender composer').waitFor({ timeout: 30000 });
const inputs = page.locator('input');
await inputs.nth(0).fill('Board Meeting Reminder');
await page.getByRole('heading', { name: 'Board Meeting Reminder' }).waitFor();
await inputs.nth(1).fill('2026-07-04');
await page.locator('.line-item strong', { hasText: '2026-07-04' }).waitFor();
await inputs.nth(2).fill('09:45');
await page.locator('.line-item strong', { hasText: '09:45' }).waitFor();
await inputs.nth(3).fill('Conference Room A');
await page.locator('.line-item strong', { hasText: 'Conference Room A' }).waitFor();
await inputs.nth(4).fill('Final approval');
await page.locator('.line-item strong', { hasText: 'Final approval' }).waitFor();
await page.locator('select').selectOption('low');
await page.locator('textarea').fill('Bring signed packet and ID.');
await page.locator('.summary', { hasText: 'Bring signed packet and ID.' }).waitFor();
await inputs.nth(5).fill('22');
await inputs.nth(6).fill('7');
await page.getByRole('button', { name: /Send Reminder/i }).first().click();
await page.getByRole('heading', { name: 'Select recipients' }).waitFor();
await page.getByRole('button', { name: /Text/i }).click();
await page.getByRole('button', { name: /Email/i }).click();
await page.screenshot({ path: 'scripts/live-site-input-test.png', fullPage: true });
console.log(JSON.stringify({ ok: true, url, testedInputs: ['title','date','time','location','milestone','urgency','notes','snooze','autoDismiss'], popup: 'Send Reminder opens modal with Email/Text choices', consoleErrors, pageErrors, screenshot: 'scripts/live-site-input-test.png' }, null, 2));
if (consoleErrors.length || pageErrors.length) process.exitCode = 1;
await browser.close();
