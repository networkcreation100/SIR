import { chromium } from 'playwright';
const url = 'https://cos-liability-spin-foot.trycloudflare.com';
const browser = await chromium.launch({ headless: true });
const page = await browser.newPage({ viewport: { width: 1440, height: 1100 } });
const consoleErrors = [];
const pageErrors = [];
page.on('console', msg => { if (msg.type() === 'error') consoleErrors.push(`${msg.type()}: ${msg.text()}`); });
page.on('pageerror', err => pageErrors.push(err.message));
await page.goto(url, { waitUntil: 'networkidle', timeout: 60000 });
await page.getByText('Sender composer').waitFor();
await page.locator('form.composer').getByRole('button', { name: 'Send reminder' }).click();
await page.getByLabel('Send options panel').waitFor();
let body = await page.locator('body').innerText();
for (const forbidden of ['Create attachment & send email', 'Create reminder & open text', 'Package + send', 'Recipient Email', 'Recipient Text (phone #)', 'Delivery path', 'Attachment-ready', 'Mobile message', 'Email recipients from the box', 'Text recipients from the box']) {
  if (body.includes(forbidden)) throw new Error(`Old send option text still visible: ${forbidden}`);
}
await page.getByText('Type a phone number or email. Phone numbers are recognized automatically and sent by text.').waitFor();
const box = page.getByRole('textbox', { name: 'Recipients' });
if ((await box.getAttribute('placeholder')) !== 'Phone number or email address') throw new Error('Recipient placeholder mismatch');
if (await page.locator('.delivery-choice').count()) throw new Error('Email/Text choice boxes still rendered');
await page.getByRole('button', { name: 'Send', exact: true }).waitFor();
if (!(await page.getByRole('button', { name: 'Send', exact: true }).isDisabled())) throw new Error('Send should be disabled with no recipient');
await box.fill('+1 808 555 1212');
await page.getByText('1 recipient ready for text.').waitFor();
await page.getByText('Text message').waitFor();
await page.getByText('+1 808 555 1212').waitFor();
if (await page.getByRole('button', { name: 'Send', exact: true }).isDisabled()) throw new Error('Send should enable for a valid phone number');
await box.fill('friend@example.com');
await page.getByText('1 recipient ready for email.').waitFor();
await page.getByText('Email', { exact: true }).waitFor();
await page.getByText('friend@example.com').waitFor();
if (await page.getByRole('button', { name: 'Send', exact: true }).isDisabled()) throw new Error('Send should enable for a valid email');
await box.fill('+1 808 555 1212, friend@example.com, nope');
await page.getByText('Use valid phone numbers or email addresses separated by commas.').waitFor();
await page.getByText('Needs correction').waitFor();
if (!(await page.getByRole('button', { name: 'Send', exact: true }).isDisabled())) throw new Error('Send should disable when any typed recipient is invalid');
await page.screenshot({ path: 'scripts/recipient-auto-detect-audit.png', fullPage: true });
console.log(JSON.stringify({ ok: true, url, covered: ['Email/Text selector boxes hidden','single Recipient box auto-detects phone','single Recipient box auto-detects email','Send button label is simplified','invalid mixed recipient disables Send','no old ambiguous button labels'], consoleErrors, pageErrors, screenshot: 'scripts/recipient-auto-detect-audit.png' }, null, 2));
if (consoleErrors.length || pageErrors.length) process.exitCode = 1;
await browser.close();
